const dictionaries: Record<Locale, Dict> = { "zh-CN": zh, ja, ko, en };

type I18nContextValue = {
  locale: Locale;
  currentLanguage: (typeof languages)[number];
  setLocale: (locale: Locale) => void;
  t: (key: string, values?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("zh-CN");

  useEffect(() => {
    const local = normalizeLocale(window.localStorage.getItem(LANGUAGE_KEY));
    if (local) setLocaleState(local);

    let active = true;
    fetchRemoteLanguagePreference().then((remote) => {
      const next = normalizeLocale(remote);
      if (!active || !next) return;
      window.localStorage.setItem(LANGUAGE_KEY, next);
      setLocaleState(next);
    });

    return () => {
      active = false;
    };
  }, []);

  function setLocale(next: Locale) {
    if (!supported.has(next)) return;
    setLocaleState(next);
    window.localStorage.setItem(LANGUAGE_KEY, next);
    document.documentElement.lang = next;
    void syncLanguagePreference(next);
  }

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    const currentLanguage = languages.find((language) => language.code === locale) ?? languages[0];
    return {
      locale,
      currentLanguage,
      setLocale,
      t: (key, values) => interpolate(dictionaries[locale][key] ?? zh[key] ?? key, values)
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}

export function normalizeLocale(value: string | null | undefined): Locale | null {
  if (!value) return null;
  return supported.has(value as Locale) ? (value as Locale) : null;
}

function interpolate(value: string, values?: Record<string, string | number>) {
  if (!values) return value;
  return value.replace(/\{\{(\w+)\}\}/g, (_, key) => String(values[key] ?? ""));
}
